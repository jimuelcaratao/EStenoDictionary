        <p class="max-w-5xl mt-10 text-center">Stenography, commonly known as shorthand or phonograph, is a method of quick writing that uses symbols in place of letters, words, and phrases (Deenen, 2017 & James, 2018). According to the study of Figueroa (n.d), it is an art of representing spoken sounds by written signs that provides a way of representing every sound heard in English words. It is a phonetic that records the sounds of speech rather than the spelling. Stenography is used in note-taking in classes, court hearings, and business meetings, especially in court hearings (James, 2018). Shorthand requires lots of effort, attention, and skills, as well as a disciplined attitude. Students’ knowledge and skills in shorthand are most likely to have an advantage in any secretarial position in the future.</p>

        <p class="max-w-5xl mt-10 text-center">The Bachelor of Science in Office Administration at the University of Caloocan City is a four-year degree program that focuses on corporate transactions, legal offices, and medical offices. This program includes skills training that is beneficial to students, such as Keyboarding and Stenography. </p>

        <p class="max-w-5xl mt-10 text-center">Today, we are now experiencing a pandemic. Most of the students and even the researchers were struggling with their studies, and it usually happened when they were in the middle of dictation. They tended to be confused and frustrated because of the unfamiliar words they'd encountered. So, to address this problem, the researchers came up with a topic that would help students develop their knowledge, expand their vocabulary, and improve their skills in terms of reading, writing strokes, and transcribing shorthand.	</p>

        <p class="max-w-5xl mt-10 text-center">In Basic Stenography, students did not pay attention and worry when they read or heard unfamiliar words because it focuses only on the fundamentals of strokes, not until they moved to Intermediate Stenography and beyond. The researchers observed and experienced those students of BSOAD encountered many unfamiliar words during their reading and dictation practices. As a result, students had a hard time reading and writing the strokes. For this reason, they struggled to transcribe and translate the unfamiliar words they heard or read. Most students struggled to write those unfamiliar words into Gregg Shorthand Volumes I and II, especially when the professor dictated them. Some of the students looked for the meaning of the unknown word in the dictionary, and some of them looked on Google or YouTube for the stroke of this word. Unfortunately, they could not find any reliable sources.	</p>

        <p class="max-w-5xl mt-10 text-center">Therefore, the researchers aimed to develop an E-Steno Dictionary to address the students' difficulties in looking for the right stroke of unfamiliar shorthand words. Today, learners increasingly depend on electronic reference materials. That’s why researchers developed an idea for the E-Steno Dictionary. Vocabulary was crucial. Communication could not happen in any meaningful sense without words to express a larger range of meanings. Chen & Hamilton (2012) stated that a dictionary is widely acknowledged as one of the most significant resources for helping students comprehend or learn terminology. Furthermore, many students not only use them frequently, but also consider them to have been beneficial. With the help of the E-Steno Dictionary, BSOAD students have been able to consolidate unknown words and bridge the vocabulary knowledge gap between them and advanced learners. Its development would aid students in broadening their vocabulary in Stenography writing.</p>
